package model.vo;

public class Scores {
	private String stuID;
	private double tutorscore;
	private double replyscore;
	private double finalscore;
	public String getStuID() {
		return stuID;
	}
	public void setStuID(String stuID) {
		this.stuID = stuID;
	}
	public double getTutorscore() {
		return tutorscore;
	}
	public void setTutorscore(double tutorscore) {
		this.tutorscore = tutorscore;
	}
	public double getReplyscore() {
		return replyscore;
	}
	public void setReplyscore(double replyscore) {
		this.replyscore = replyscore;
	}
	public double getFinalscore() {
		return finalscore;
	}
	public void setFinalscore(double finalscore) {
		this.finalscore = finalscore;
	}
	
}

